# -- Types Ruby --
# Type of variable is defined when it receive a value

product = 'Macbook Air'
price = 5999.99
amount = 10
available = true

puts 'TYPES'
puts product.class
puts price.class
puts amount.class
puts available.class

puts 'VALUES'
puts product
puts price
puts amount
puts available

amount = '9'

puts 'CHANGE TYPE'
puts amount.class